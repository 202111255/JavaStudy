public class DataType06 {
    public static void main(String[] args) {
        //실수
        //float, double(*)

        double d = 2.5;
        System.out.println(d);

        float f = 2.5f;
        System.out.println(f);

        //정수 => 실수 => 지수
        //1000 -> 1.0e3
        double d2 = 1.0e3;
        System.out.println(d2);
    }
}
